    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        String Kode = (String) jComboBox2.getSelectedItem();
        String KodeFix = Kode.substring(0,4);
        try{
            Statement St = Koneksi.getConnection().createStatement();
            ResultSet RS = St.executeQuery("SELECT Stock_Barang,Harga_Barang FROM Barang where Kode_Barang = '" + KodeFix + "'");
            while(RS.next()){
                jTextField5.setText(RS.getString("Stock_Barang"));
                jTextField6.setText(RS.getString("Harga_Barang"));
            }
        }catch(Exception e){
            e.printStackTrace();
        }// TODO add your handling code here:
    }//GEN-LAST:event_jComboBox2ItemStateChanged

