    private void hitungTotalBayar(){
        int JumlahRow = jTable1.getRowCount();
        int Total = 0;
        if(JumlahRow == 0){
            return;
        }else{
            for(int i = 0; i < JumlahRow; i++){
                int JumlahTotal = Integer.valueOf((String.valueOf(Model.getValueAt(i, 4))));
                Total += JumlahTotal;
            }
        }
        jTextField8.setText(String.valueOf(Total));
    }
