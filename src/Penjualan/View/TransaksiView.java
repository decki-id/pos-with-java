    public void NomorFaktur(){
        TransaksiImplement getUrutan = new TransaksiImplement();
        int Urutan = getUrutan.UrutanDatabase();
        Calendar Now = Calendar.getInstance();
        String Kode_Awal = "PO";
        int Year = Now.get(Calendar.YEAR);
        int Month = Now.get(Calendar.MONTH);
        int Day = Now.get(Calendar.DAY_OF_MONTH);
        StringBuffer StringValues = new StringBuffer();
        StringValues.append(Kode_Awal);
        StringValues.append("-");
        StringValues.append(String.valueOf(Year));
        StringValues.append(String.valueOf(Month));
        StringValues.append(String.valueOf(Day));
        StringValues.append("-");
        StringValues.append(String.valueOf(Urutan));
        String Result = StringValues.toString();
        jTextField1.setText(Result);
    }
    
