    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String Kode = (String) jComboBox2.getSelectedItem();
        String KodeFix = Kode.substring(0,4);
        String NamaBarang = Kode.substring(5, Kode.length()-0);
        int Harga = Integer.valueOf(jTextField6.getText());
        int Jumlah = Integer.valueOf(jTextField7.getText());
        long TotalPembelian = Harga * Jumlah;

        Object[] Data = new Object[5];
        Data [0] = KodeFix;
        Data [1] = NamaBarang;
        Data [2] = jTextField6.getText();
        Data [3] = jTextField7.getText();
        Data [4] = TotalPembelian;

        Model.addRow(Data);
        this.hitungTotalBayar();
        this.kosongkanDataBarang();// TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

